import React from 'react';
import { Form } from 'react-bootstrap';

const Time = ({ data, handleChange, handleRemove }) => {
  return (
    <li className="d-flex">
      <Form.Control
        required
        type="time"
        name="times"
        className="form-input"
        onChange={(e) => {
          handleChange(e, data.index);
        }}
        defaultValue={data.time}
      ></Form.Control>
      {data.index !== 0 ? (
        <button
          type="button"
          className="btn-close"
          aria-label="Close"
          onClick={(e) => handleRemove()}
        ></button>
      ) : null}
    </li>
  );
};

export default Time;
